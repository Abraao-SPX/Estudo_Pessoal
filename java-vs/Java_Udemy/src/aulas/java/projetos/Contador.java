package projetos;

public class Contador {
    public static void main(String[] args) {
        // O loop for começa em 1, continua enquanto i for menor ou igual a 10000000,
        // e incrementa i em 1 a cada iteração.
        for (int i = 1; i <= 10000000; i++) {
            System.out.println(i);
        }
    }
}